"""
Дано: пользователь делает вклад в размере X рублей сроком на Y лет под 10% годовых (каждый год размер его вклада увеличивается на 10%, эти деньги прибавляются к сумме вклада, и на них в следующем году тоже будут проценты).
Задача: написать функцию 
bank, принимающую аргументы X и Y и возвращающую сумму, которая будет на счету пользователя спустя Y лет.
"""


def bank(X, Y):
    for _ in range(Y):
        X += X * 0.10
    return X


# Example usage
initial_deposit = 1000
years = 5
final_balance = bank(initial_deposit, years)
print(f"Сумма на счету пользователя спустя {years} лет: {final_balance} рублей")
